#ifndef __VLOG_DM_DECLARED_VISITOR_HPP__
#define __VLOG_DM_DECLARED_VISITOR_HPP__

/***************************************************************************/

namespace VlogDM {

/***************************************************************************/

struct Port;
struct Variable;

/***************************************************************************/

struct DeclaredVisitor
	:	public boost::noncopyable
{

/***************************************************************************/

	virtual void visit( Port const& _port ) = 0;

	virtual void visit( Variable const& _var ) = 0;

/***************************************************************************/

};

/***************************************************************************/

struct DeclaredDefaultVisitor
	:	public DeclaredVisitor
{

/***************************************************************************/

	void visit( Port const& _port ) override {}
	
	void visit( Variable const& _var ) override {}

/***************************************************************************/

};

/***************************************************************************/

}

/***************************************************************************/

#endif // !__VLOG_DM_DECLARED_VISITOR_HPP__
